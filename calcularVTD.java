package com.ejerciciosPracticos.diplomadoJava;

public class calcularVTD {
	
	
	
	
	public double distancia(double v, double t) {
		
		double result = v*t;
		return result;
		
	}
	
	
	public double tiempo (double d, double v) {
		
		double result = d / v;
		return result;
		
	}
	
	public double velocidad (double d, double t) {
		
		double result = d / t;
		return result;
	}
	

}
